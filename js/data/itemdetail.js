/* catalog item → {views, appeals} (없으면 규칙 추천, 그래도 없으면 범용) */
function gfItemDetail(c) {
  if (!c) return { views:'정면·측면·후면, 표면/재질 디테일, 패키지컷, 사용예시, 사이즈 비교', appeals:['제품 고유 매력'] };
  if (GF_ITEM_DETAIL[c.id]) return GF_ITEM_DETAIL[c.id];
  var text = (c.name || '') + ' ' + (c.desc || '');
  for (var i = 0; i < GF_DETAIL_RULES.length; i++) {
    var r = GF_DETAIL_RULES[i];
    for (var j = 0; j < r.kw.length; j++) {
      if (text.indexOf(r.kw[j]) >= 0) return { views: r.views, appeals: r.appeals };
    }
  }
  return { views:'정면·측면·후면, 표면/재질 디테일, 패키지컷, 사용예시, 사이즈 비교', appeals:['제품 고유 매력'] };
}
